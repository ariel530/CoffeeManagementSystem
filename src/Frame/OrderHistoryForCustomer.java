    void displayTableData(ArrayList<Order> orderData) {
        orderTable.removeAll();
        Object[][] data = new Object[orderData.size()][];
        for (int i = 0; i < orderData.size(); i++) {
            data[i] = getObjectArray((i + 1),
                    orderData.get(i).getId(), orderData.get(i).getName(), orderData.get(i).getTotalPrice(), orderData.get(i).getDate());
        }
        String[] columns = {
            "SNo", "Id", "Name", "Price", "Date"
        };
        DefaultTableModel tmodel = new DefaultTableModel(data, columns) {
            @Override
            public Class getColumnClass(int column) {
                return getValueAt(0, column).getClass();
            }
        };
        orderTable.setModel(tmodel);
        orderTable.getColumn("SNo").setPreferredWidth(35);
        orderTable.getColumn("Id").setPreferredWidth(35);
        orderTable.setRowHeight(30);
    }

